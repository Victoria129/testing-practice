class calculator {
  static add(a,b) {
    let sum = a + b;
    return sum;
  }

  static subtract(a,b) {
    let difference = a - b;
    return difference;
  }

  static multiply(a,b) {
    let product = a * b;
    return product;
  }

  static divide(a,b) {
    let quotient = a / b;
    return quotient;
  }
}

module.exports = calculator;
  
